drop policy if exists "company_admin_read_companies" on public.companies;
drop policy if exists "company_admin_insert_companies" on public.companies;
drop policy if exists "company_admin_update_companies" on public.companies;
drop policy if exists "allow_read_companies" on public.companies;
drop policy if exists "allow_insert_companies" on public.companies;
drop policy if exists "allow_update_companies" on public.companies;

create policy "company_admin_read_companies"
on public.companies
for select
to authenticated
using (
  public.user_is_platform_admin()
  or public.user_is_company_member(id)
);

create policy "company_admin_insert_companies"
on public.companies
for insert
to authenticated
with check (
  public.user_is_platform_admin()
);

create policy "company_admin_update_companies"
on public.companies
for update
to authenticated
using (
  public.user_is_platform_admin()
  or public.user_is_company_admin(id)
)
with check (
  public.user_is_platform_admin()
  or public.user_is_company_admin(id)
);

