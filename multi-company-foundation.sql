do $$
declare
  default_company_id uuid;
begin
  select id into default_company_id
  from public.companies
  order by created_at asc
  limit 1;

  if default_company_id is not null then
    update public.company_settings set company_id = default_company_id where company_id is null;
    update public.drivers set company_id = default_company_id where company_id is null;
    update public.driver_compliance set company_id = default_company_id where company_id is null;
    update public.trucks set company_id = default_company_id where company_id is null;
    update public.loads set company_id = default_company_id where company_id is null;
    update public.assignments set company_id = default_company_id where company_id is null;
    update public.maintenance_logs set company_id = default_company_id where company_id is null;
    update public.alerts set company_id = default_company_id where company_id is null;
    update public.customers set company_id = default_company_id where company_id is null;
    update public.invoices set company_id = default_company_id where company_id is null;
    update public.load_events set company_id = default_company_id where company_id is null;
    update public.load_documents set company_id = default_company_id where company_id is null;
    update public.settlements set company_id = default_company_id where company_id is null;
    update public.profiles set company_id = default_company_id where company_id is null;
  end if;
end $$;

