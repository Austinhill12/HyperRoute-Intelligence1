-- HyperRoute Intelligence
-- Multi-company SaaS foundation, phase 1.
--
-- This script adds company/account structure without removing your current
-- working public policies. Run this first, confirm the app still works, then
-- update application code and stricter RLS policies in the next phase.

create table if not exists public.companies (
  id uuid primary key default gen_random_uuid(),
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  company_name text not null,
  legal_name text,
  phone text,
  email text,
  website text,
  address_line_1 text,
  address_line_2 text,
  city text,
  state text,
  zip text,
  mc_number text,
  dot_number text,
  status text not null default 'active'
);

alter table public.companies enable row level security;

drop policy if exists "allow_read_companies" on public.companies;
create policy "allow_read_companies"
on public.companies
for select
to public
using (true);

drop policy if exists "allow_insert_companies" on public.companies;
create policy "allow_insert_companies"
on public.companies
for insert
to public
with check (true);

drop policy if exists "allow_update_companies" on public.companies;
create policy "allow_update_companies"
on public.companies
for update
to public
using (true)
with check (true);

insert into public.companies (
  company_name,
  legal_name,
  status
)
select
  coalesce((select company_name from public.company_settings where id = 1), 'HyperRoute Intelligence'),
  coalesce((select company_name from public.company_settings where id = 1), 'HyperRoute Intelligence'),
  'active'
where not exists (
  select 1 from public.companies
);

create table if not exists public.company_users (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone not null default now(),
  company_id uuid not null references public.companies(id) on delete cascade,
  user_id uuid not null,
  role text not null default 'dispatcher',
  status text not null default 'active',
  unique (company_id, user_id)
);

alter table public.company_users enable row level security;

drop policy if exists "allow_read_company_users" on public.company_users;
